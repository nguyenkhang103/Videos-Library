package com.example.kaido.videoslibrary.models;

public class PlaylistModel {
    String titlePlaylist;
    String dateTimePlaylist;
    String id;
    String thumnails;

    public String getTitlePlaylist() {
        return titlePlaylist;
    }

    public void setTitlePlaylist(String titlePlaylist) {
        this.titlePlaylist = titlePlaylist;
    }

    public String getDateTimePlaylist() {
        return dateTimePlaylist;
    }

    public void setDateTimePlaylist(String dateTimePlaylist) {
        this.dateTimePlaylist = dateTimePlaylist;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getThumnails() {
        return thumnails;
    }

    public void setThumnails(String thumnails) {
        this.thumnails = thumnails;
    }
}
